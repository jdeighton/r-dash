-- Create Products Table
CREATE TABLE products (
  id INTEGER PRIMARY KEY,
  name VARCHAR,
  category VARCHAR,
  price DECIMAL(10, 2),
  stock INTEGER
);

-- Create Customers Table
CREATE TABLE customers (
  id INTEGER PRIMARY KEY,
  name VARCHAR,
  email VARCHAR,
  city VARCHAR,
  total_orders INTEGER
);

-- Create Sales Table
CREATE TABLE sales (
  id INTEGER PRIMARY KEY,
  date DATE,
  product_id INTEGER,
  customer_id INTEGER,
  quantity INTEGER,
  amount DECIMAL(10, 2)
);

-- Create Events Table
-- start_date and event_name are required; end_date, event_description, customer_id are optional.
-- end_date present = a multi-day period; absent = a single-day event.
-- customer_id references customers(id) but no FK is enforced.
CREATE TABLE events (
  id INTEGER PRIMARY KEY,
  start_date DATE NOT NULL,
  end_date DATE,
  event_name VARCHAR NOT NULL,
  event_description VARCHAR,
  customer_id INTEGER
);

